    //Coje todos lo nombres en el hashmap de todos los pokemons y los añade a una arraylist. Despues la arraylist añade los nombres al combobox 
    public void getAllPokemon(){
      listaNombres.clear();
        for (String o : todosPokemonMap.keySet()) {
            String s = todosPokemonMap.get(o).getNombre();
            listaNombres.add(s);
        }
        System.out.println(listaNombres);
        jComboBox1.setModel(new DefaultComboBoxModel(listaNombres.toArray()));
    }
    
    //Mira si el pokemon es capturable llamando el metodo de catchable para ver si se puede atrapar con un true(atrapado) o false(no atrapado)
    public void catchSelectedPoke(){
        String tempSelected = jComboBox1.getSelectedItem().toString();
        for (Pokemon o : todosPokemonMap.values()) {
                if (tempSelected.equals(todosPokemonMap.get(o.getNombre()).getNombre())) {
                    if(o.catchable() == true){
                        jLabel3.setVisible(true);
                        jLabel4.setVisible(false);
                    }
                    else{
                        jLabel3.setVisible(false);
                        jLabel4.setVisible(true);
                    }
                } else {
                }
            }
    }

