    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tipoLista = jComboBox1.getSelectedItem().toString();

        switch (tipoLista) {
            case " ":
                getAllPokemon();
                break;
            case "Agua":
                getAllWater();
                break;
            case "Fuego":
                getAllFire();
                break;
            case "Planta":
                getAllPlant();
                break;

        }


    }//GEN-LAST:event_jButton1ActionPerformed


